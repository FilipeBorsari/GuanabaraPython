#Criar um programa que receba 2 numeroes e realize a soma entre eles

num1 = int(input ('Escolha o primeiro número: '))
num2 = int(input ('Escolha o segundo número: '))
soma = num1+num2


print ('o resultado entre a soma de {} e {} é {}'.format(num1,num2,soma))

#podemos usar {} e depois o comando .format(x,x,x,x,x,x) para colocar as informações na ordem
